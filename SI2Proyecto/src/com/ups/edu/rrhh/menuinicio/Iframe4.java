    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtIdVac.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "INGRESE CODIGO DE VACACIONES","Error",JOptionPane.WARNING_MESSAGE);
        }else if (txtIdPers.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "INGRESE CODIGO PERSONAL","Error",JOptionPane.WARNING_MESSAGE);
        }else if (txtdias.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "INGRESE NUMERO DE DIAS","Error",JOptionPane.WARNING_MESSAGE);
        }else if (txtfechainicio.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "INGRESE FECHA DE INICIO","Error",JOptionPane.WARNING_MESSAGE);
        }else if (txtfechafin.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "INGRESE FECHA DE CULMINACION","Error",JOptionPane.WARNING_MESSAGE);
        }else{
            daoVacaciones obj = new daoVacaciones();
            int a = Integer.parseInt(txtIdVac.getText()); 
            int b = Integer.parseInt(txtIdPers.getText()); 
            int c = Integer.parseInt(txtdias.getText());
            Date d = Date.valueOf(txtfechainicio.getText());
            Date e = Date.valueOf(txtfechafin.getText());
            obj.Insertar(new vacaciones(a,b,c,d,e));
            txtIdVac.setText("");
            txtIdPers.setText("");
            txtdias.setText("");
            txtfechainicio.setText("");
            txtfechafin.setText("");        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

