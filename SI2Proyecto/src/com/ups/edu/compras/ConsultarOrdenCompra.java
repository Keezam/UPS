    public void getOrdenCompra(){
        String query = "";
        try{
            tblOrdenCompra.setModel(modelo);
            query = "SELECT `id_orden_compra`, `fecha_orden`, `fecha_atencion`, `estado` FROM `cmprv_orden_compra`";
            Statement s = conn.createStatement();
            ResultSet rs = s.executeQuery(query);
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            for (int i = 1; i <= cantidadColumnas; i++) {
               modelo.addColumn(rsMd.getColumnLabel(i));
            }
            while (rs.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                  fila[i]=rs.getObject(i+1);
                }
                modelo.addRow(fila);
            }
            rs.close();
        }catch(Exception e){
        }
    }
    
    private void cargarPorCheck(){
        String query = "SELECT `id_orden_compra`, `fecha_orden`, `fecha_atencion`, `estado` FROM `cmprv_orden_compra` ";
        String condicion = "";
        if(checkAProbada.isSelected() && checkDeclinada.isSelected() && checkDeclinada.isSelected()){
            getOrdenCompra();
        }else if(checkAProbada.isSelected() && checkDeclinada.isSelected()){
            condicion = "where `estado` = 'A' & `estado` = 'D'";
        }else if(checkAProbada.isSelected() && checkEspera.isSelected()){
            condicion = "where `estado` = 'A' & `estado` = 'E'";
        }else if(checkDeclinada.isSelected() && checkEspera.isSelected()){
            condicion = "where `estado` = 'D' & `estado` = 'E'";
        }else if(checkAProbada.isSelected()){
            condicion = "where `estado` = 'A'";
        }else if(checkDeclinada.isSelected()){
            condicion = "where `estado` = 'D'";
        }else if(checkEspera.isSelected()){
            condicion = "where `estado` = 'E'";
        }
        try{
            tblOrdenCompra.setModel(modelo);
            Statement s = conn.createStatement();
            ResultSet rs = s.executeQuery(query + condicion);
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            for (int i = 1; i <= cantidadColumnas; i++) {
               modelo.addColumn(rsMd.getColumnLabel(i));
            }
            while (rs.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                  fila[i]=rs.getObject(i+1);
                }
                modelo.addRow(fila);
            }
            rs.close();
        }catch(Exception e){
            
        }
    }
    
